n=int(input("Введіть ціле число n"))#Просимо значення n від користувача
y=0
for i in range(0, n):#Арифметичний цикл, в якому лічильник і набуває значень від 0 до n
    y=((3*(n-i)+y)**(1/2))#Обраховуємо значення виразу для кожного n у зворотньому напрямку

print("Значення y =", y)#Виводимо значення виразу
